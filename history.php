                <h3 class="main-heading1">Madarsa of Ghaziuddin Khan</h3>
                <p>Ghazi uddin&#39;s son was Nizam-ul-Mulk, the founder of the Nizam dynasty at Hyderabad. The Madrassa
                    was founded by Ghazi-uddin and is still running as an educational institution now known as the
                    Anglo-
                    Arabic Senior Secondary School/Zakir Hussian Delhi College at Ajmeri Gate, Delhi. The
                    double-storeyed
                    building has been constructed around a large courtyard and was outset through an imposing red
                    sandstone gateway.
                </p>
                <h3 class="main-heading1">From 1872 to 1908</h3>
                <p>
                    Anglo Arabic School is not the name of a single entity institution, instead in the different eras of
                    history several educational institutions were established under this name at various places in
                    Delhi. The first school of this succession working under Eitmatud-Daula Trust came into
                    existence with the name of Arabic Middle School at Chandni Mahal in 1872, as at that time the
                    historical building of Madarsa Ghaziuddin, situated at Ajmeri Gate was being used as police
                    constabulary since the mutiny of 1857 and the government did not concede to vacate it. Then
                    Mr. Syed Ameer Ali, who was an intelligent and promising old boy of Delhi college was
                    designated as the head master of this Madarsa, which then upgraded up to High school in
                    1884 and then it was transferred to Haveli Ikramullah Khan in Mohalla Sirkiwalan. Nowadays the
                    building of Excelsior cinema exists. In times little prior to it, Khwaja Shihabuddin Dehelvi had
                    been appointed as the head master and Maulana Altaf Husain Hali had also arrived being
                    appointed as a teacher. During the period of Principal-ship of Khwaja Shihabuddin, the school
                    flourished at an extra ordinary pace and progressed enormously. Its discipline was considered
                    ideal. Even in sports and games competitions, the students of the school earned a distinct fame.
                    The football shield had destined permanently year after year. Due to all these reasons, the
                    school had become favourite among the people.
                </p>
                <p>
                    In 1892, the government conceded the old demand of public and vacated the historical building
                    of Madarsa Ghaziuddin for the school. This occasion was celebrated in the form of a ceremony
                    with a great splendour. A royal court for meeting the general audience was held at
                    government expenses. The governor was appointed as the chief guest for the function. For the
                    ceremony Maulana Hali who was already serving as a teacher composed a poem, which was sung in chorus
                    by the students. Khwaja Shihabuddin expired in 1908 and Maulvi Fazaluddin was given the charge
                    of the head master in the institution.
                </p>
                <h3 class="main-heading1">From 1908 to 1917</h3>
                <p>
                    During this period a great extension and progress was achieved in all these educational
                    institutions. The Principal-ship of Maulvi Fazaluddin (B.A.) proved successful in all the
                    aspects. In fact he had been serving this school as a teacher since a long time, resulting him
                    being considered a renowned person by Delhites and following that he took the charge of
                    heading the institution under the redundant passion of service. Thus numerous changes and a
                    lot of progress were seen in this duration. At that time there was a moat present around the
                    Madarsa which was filled with soil and a smooth ground was developed for the students, so that
                    they could play different games. Now due to the availability of the ground a great interest for
                    games was developed in the students. By adopting football as their favorite game, they
                    emerged as champions and earned a great fame in this field. It was seen that the shield of inter
                    school state tournaments for football remained a permanent fixture of this school years after
                    year.
                </p>
                <h3 class="main-heading1">From the stage of Anglo Arabic School to Anglo Arabic College</h3>
                <p>
                    With the help of special grant from the government and the invaluable donation from
                    Honorable Nizam of Hyderabad (Deccan), a huge hall was constructed in 1917 at the South of
                    the school building. Further in 1924, the Nizam donated seventeen thousand rupees which
                    helped in the construction of three big and two small rooms. In 1924 some more class rooms
                    were constructed on the upper level of the gate in which classes for F. A. started and the college
                    got affiliated with the Delhi University. Owing to the extension of the building, the school was
                    upgraded to an intermediate college and the students of class (F. A.) appeared for the first time
                    in the university exams. Prior to it, other branches of Anglo Arabic Primary Schools had been
                    established subsequently in Kucha Chelan, Mohalla Tokriwalan and in Gali Qasim Jan. Together
                    with it, a lower middle school was established concurrently in Mohalla Churiwalan. After
                    sometime the Primary School of Kucha Chelan was upgraded to a middle school. Maulvi Fazaluddin
                    continued to work as the headmaster up to March 1927 but in the month of April
                    a new Principal was appointed in his place.
                </p>
                <h3 class="main-heading1">From 1927 to 1947</h3>
                <p>
                    On 14th April, 1927 Maulvi Fazaluddin handed over the charge of Principal-ship to the new
                    Principal Mr. C. R. Walker. Earlier Mr. Walker was working as a teacher in a military school of
                    Ghoragli, near the Hill Station Murrey, which is now a part of Pakistan. By his enthusiastic and
                    continued efforts, this school earned a great fame and at that time it was an example of
                    disciplined institution in itself. In results, it was considered equally good to the reputed and
                    renowned schools of the city. It earned a great name and fame in the field of sports, games and
                    scouting. As Mr. Walker toiled day and night, he made other teachers work hard. The teachers
                    also assisted him fully. Hence all the three Arabic Schools achieved a good name which was
                    considered as a surety for good education and character building among Muslim
                    children.
                </p>
                <p>In 1927 Khan Bahadur Nawabul Hasan Khan ordered to construct a bungalow bearing all the
                    expenditure himself for the Principal&#39;s residence in the south western portion of the college
                    building, where no Principal opted to reside ever. Of course different offices related to the
                    college working were established in this portion. In 1933 adjacent to the old building of the
                    college, six big and four small rooms were constructed. The eminent spiritual leader of Bohra
                    Community Mr. Tahir Saifuddin donated one thousand rupees for its construction. Afterward
                    the electric fans were also fitted in these rooms.</p>
                <p>
                    In 1929 the middle school of Kucha Chelan, Darya Ganjgot upgraded to a High School. In
                    October 1929 there was an addition of degree classes in the college. After becoming a degree
                    college according to the university rules, Mr. Walker recruited more teachers. From 1943 an
                    arrangement for teaching (M.A.) classes started. In the tumult of September 1947, the college
                    had to be shut down and in this way the era of college by the name of Anglo Arabic College
                    came to an end.
                </p>
                <p>
                    When circumstances improved reasonably, the energetic efforts of the former old boy of Anglo
                    Arabic College,Mirza Mahmood Baig(who afterwards became the Professor of Philosophy in the
                    same college) proved fruitful and resulted in starting both the institutions at Ajmeri Gate once
                    again. The school began to work with its old name, Anglo Arabic Higher Secondary School but
                    the name of the college changed from Anglo Arabic College to Delhi College. Since then both these
                    institutions are performing their work freely but separately. Afterwards in 1975 the name of
                    Delhi College was changed once again and it was renamed as Dr. Zakir Husain College but in 2012
                    its name has become Dr. Zakir Husain Delhi College due to the efforts of some of the old boys of
                    the college. This college is functioning till now by the same name though now, in a new campus
                    at Minto Road. After the enforcement of new education (10 + 2) policy the name of the school
                    has also been changed to Anglo Arabic Senior Secondary School and it has been working till now
                    in the historical Madarsa Ghaziuddin building at Ajmeri Gate.
                </p>

